void LoadMap() {
    track = loadTexture("assets/track.png");
    grass = loadTexture("assets/grass.png");
    src.x = src.y = 0;
    src.w = dest.w = 50;
    src.h = dest.h = 50;
}

void DrawMap() {

    int type = 0;
    for (int i = 0; i < 18; i++) {
        for (int j = 0; j < 32; j++) {
            type = map[i][j];
            dest.y = i * 50;
            dest.x = j * 50;
            switch (type) {
                case 0:
                    Draw(grass, src, dest);
                    break;
                case 1:
                    Draw(track, src, dest);
                    break;
                default:
                    break;
            }
        }
    }

}